package com.github.log.exception;

public class FilterException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6930784739272687591L;

	public FilterException() {
		super();
	}

	public FilterException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FilterException(String message, Throwable cause) {
		super(message, cause);
	}

	public FilterException(String message) {
		super(message);
	}

	public FilterException(Throwable cause) {
		super(cause);
	}
	
	

}
